fav_color = 'pink'

case fav_color
	when 'red'
		puts "red like fire"
	when 'orange'
		puts "orange like, well... an orange."
	when 'yellow'
		puts "yellow daffodils are so pretty in the spring"
	when 'green'
		puts "have  you been to the emerald city in oz?"
	when 'blue'
		puts "blue like the sky"
	when 'purple'
		puts "purple plums are the tastiest"
	else
		puts "hm, i don't know that color"
	end

current_weather = "rainy"

case current_weather
	when 'rainy'
		puts "bring an umbrella!"
	when 'sunny'
		puts "nice day out there!"
	when 'cloudy'
		puts "could rain"
	when 'foggy'
		puts "how mysterious!"
	when 'snowy'
		puts 'burr'
	else puts "get ready for anything!"
	end


